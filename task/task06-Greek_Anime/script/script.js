const exibir = document.getElementById("list1");
const exibirLs = document.getElementById("ls1");


exibir.addEventListener('click', () => {

  if (exibirLs.style.display === 'block') {
    exibirLs.style.display = 'none';
  } 
  
  else {
    exibirLs.style.display = 'block';
  }

});

